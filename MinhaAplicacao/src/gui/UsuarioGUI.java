    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       Usuario usuarios = new Usuario();
       usuarios.setNome(jTextField1.getText());
       usuarios.setCpf(jTextField2.getText());
       usuarios.setTelefone(jTextField3.getText());
       usuarios.setEmail(jTextField4.getText());
       
       //FAZENDO A VALIDAÇÃO DOS DADOS
       if((jTextField1.getText().isEmpty()) || (jTextField2.getText().isEmpty()) || (jTextField3.getText().isEmpty()) || (jTextField4.getText().isEmpty())){
           JOptionPane.showMessageDialog(null,"Os campos não podem retornar vazio!");
    } else{
           //INSTANCIANDO A CLASSE UsuarioDAO  
           UsuarioDAO dao = new UsuarioDAO();
           dao.adiciona(usuarios);
           JOptionPane.showMessageDialog(null, "Usuáirio "+jTextField1.getText()+" inserido com sucesso!");
       }
       
       //APAGA OS DADOS PREENCHIDOS NOS CAMPOS DE TEXTO
       jTextField1.setText("");
       jTextField2.setText("");
       jTextField3.setText("");
       jTextField4.setText("");
      
       
        
    }//GEN-LAST:event_jButton1ActionPerformed

